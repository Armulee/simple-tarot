export function isSupportedNoticeLanguage(
    value: string,
): value is NoticeLanguage {
    return value === "en" || value === "th" || value === "lo"
}

export function getNoticeTranslations(language: string): NoticeTranslations {
    if (isSupportedNoticeLanguage(language)) {
        return NOTICE_TRANSLATIONS[language]
    }
    return NOTICE_TRANSLATIONS.en
}

export async function loadNoticeTranslations(
    language: string,
): Promise<NoticeTranslations> {
    return getNoticeTranslations(language)
}
