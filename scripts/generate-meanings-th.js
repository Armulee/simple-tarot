function minorYesNo(suit, orientation) {
    if (orientation === "upright") {
        if (suit === "wands" || suit === "pentacles") return "Yes"
        return suit === "cups" ? "Maybe" : "No"
    }
    if (suit === "wands" || suit === "pentacles") return "Not yet"
    return "No"
}

function buildCardEntry(card) {
    const { slug, arcana, suit, rank } = card
    const sections = ["relationships", "work", "finance", "health"]
    const upMeta = arcana === "major" ? majorMeta(slug) : null
    const overviewMeta = (orientation) => ({
        yesNo: YESNO_TH[arcana === "major"
            ? (orientation === "reversed" && upMeta.yesNo === "Yes" ? "Not yet" : upMeta.yesNo)
            : minorYesNo(suit, orientation)] || "",
        zodiac: arcana === "major" ? upMeta.zodiac : suitLabel(suit),
        element: arcana === "major" ? (MAJOR_ELEMENT[slug] || undefined) : suitElementName(suit),
    })
    const entry = {
        slug,
        upright: { overview: { keywords: buildOverviewKeywords(card, "upright"), text: overviewText({ slug, arcana, suit, rank, orientation: "upright" }), ...overviewMeta("upright") } },
        reversed: { overview: { keywords: buildOverviewKeywords(card, "reversed"), text: overviewText({ slug, arcana, suit, rank, orientation: "reversed" }), ...overviewMeta("reversed") } },
    }
    for (const section of sections) {
        entry.upright[section] = { keywords: buildSectionKeywords(card, "upright", section), text: longSectionText({ slug, arcana, suit, rank, orientation: "upright", section }) }
        entry.reversed[section] = { keywords: buildSectionKeywords(card, "reversed", section), text: longSectionText({ slug, arcana, suit, rank, orientation: "reversed", section }) }
    }
    return entry
}

function titleCase(s) { return s.charAt(0).toUpperCase() + s.slice(1) }

function buildAll() {
    const items = {}
    majorNames.forEach((name) => {
        const slug = slugify(name)
        items[slug] = buildCardEntry({ name, slug, arcana: "major", suit: null, rank: null })
    })
    suits.forEach((suit) => {
        ranks.forEach((rank) => {
            const name = `${rank} of ${titleCase(suit)}`
            const slug = slugify(name)
            items[slug] = buildCardEntry({ name, slug, arcana: "minor", suit, rank })
        })
    })
    return items
}

function main() {
    const generated = buildAll()
    if (!fs.existsSync(OUTPUT_DIR)) fs.mkdirSync(OUTPUT_DIR, { recursive: true })
    for (const [slug, data] of Object.entries(generated)) {
        fs.writeFileSync(path.join(OUTPUT_DIR, `${slug}.json`), JSON.stringify(data, null, 2))
    }
    console.log("Wrote Thai meanings to", OUTPUT_DIR, `(${Object.keys(generated).length} cards)`)
}

main()
